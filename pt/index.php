	                      <u>Bem vindo a One World Academy</u>!<br><br>
	                      One World Academy é um escola primária privada com o objetivo de proporcionar uma educação internacionalmente competitiva a um preço acessível. Estamos atualmente no nosso 4º ano de existência, oferecendo ensino bilíngüe para as classes <span style="padding: 5px;" class="mdl-color-text--green"><u>Pré-Escolar</u></span>, <span style="padding: 5px;" class="mdl-color-text--green"><u>1ª</u></span>, <span style="padding: 5px;" class="mdl-color-text--green"><u>2ª</u></span>, <span style="padding: 5px;" class="mdl-color-text--green"><u>3ª</u></span> e a <span style="padding: 5px;" class="mdl-color-text--green"><u>4ª Classe</u></span>.
	                      A cada ano que passa vamos adicionando uma classe nova, o que nos permite garantir que a <i>One World Academy</i> possa manter a qualidade que nos tornou tão bem conhecidos.
												<br><br>
	                      O nosso currículo escolar é baseado no <i>Currículo Nacional Moçambicano</i>, que é ensinado na língua portuguesa durante o período da manhã. No período da tarde, chega uma nova equipe de professores e a língua de instrução para todas as classes muda para o Inglês onde usamos o <i>Currículo Cambridge</i>. Na <i>One World Academy</i>, estamos empenhados em oferecer uma sólida conquista acadêmica com foco em alfabetização, numeracia e tecnologia. Utilizamos uma abordagem de investigação integrada para o nosso planejamento académico que busca relacionar o conteúdo curricular entre diferentes assuntos de maneira significativa para os nossos alunos.
												<br><br>
	                      A aprendizagem na <i>One World Academy</i> é baseada pelos nossos valores de <span style="padding: 5px;" class="mdl-color-text--brown-400"><u>Respeito</u></span>, <span style="padding: 5px;" class="mdl-color-text--orange-400"><u>Excelência</u></span> e <span style="padding: 5px;" class="mdl-color-text--red-400"><u>Responsabilidade</u></span>. Nós usamos estes valores na orientação dos nossos comportamentos e desenvolvemos uma relação respeitosa assim como uma aprendizagem efetiva.
